package com.ws.main.model;

public class Shows {
	
	private int id;
	private String name;
	private String date;
	private String time;
	private String availableTickets;
	private String price;
	private String type;
	private String multiplex;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getAvailableTickets() {
		return availableTickets;
	}
	public void setAvailableTickets(String availableTickets) {
		this.availableTickets = availableTickets;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getMultiplex() {
		return multiplex;
	}
	public void setMultiplex(String multiplex) {
		this.multiplex = multiplex;
	}


}
